package com.reerinkresearch.bag.model;

public class Summary {

	private int numGemeenten;
	private int numWoonplaatsen;
	private int numStraten;
	private int numAdressen;
	
	public int getNumGemeenten() {
		return numGemeenten;
	}
	
	public void setNumGemeenten(int numGemeenten) {
		this.numGemeenten = numGemeenten;
	}

	public int getNumWoonplaatsen() {
		return numWoonplaatsen;
	}

	public void setNumWoonplaatsen(int numWoonplaatsen) {
		this.numWoonplaatsen = numWoonplaatsen;
	}

	public int getNumStraten() {
		return numStraten;
	}

	public void setNumStraten(int numStraten) {
		this.numStraten = numStraten;
	}

	public int getNumAdressen() {
		return numAdressen;
	}

	public void setNumAdressen(int numAdressen) {
		this.numAdressen = numAdressen;
	}
	
}
